          <el-select v-model="form.type" placeholder="请选择0: 自行注册；1: 后台创建">
            <el-option
              v-for="dict in dict.type.sys_signup_type"
              :key="dict.value"
              :label="dict.label"
              :value="dict.value"
            ></el-option>
          </el-select>
        </el-form-item>
        <el-form-item label="${comment}" prop="signupTime">
